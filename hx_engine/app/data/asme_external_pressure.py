# ---------------------------------------------------------------------------
# Interpolation helpers
# ---------------------------------------------------------------------------

def _log_interp(x: float, x0: float, x1: float, y0: float, y1: float) -> float:
    """Log-log linear interpolation between two points."""
    if x0 <= 0 or x1 <= 0 or y0 <= 0 or y1 <= 0 or x <= 0:
        # Fallback to linear if any value is non-positive
        frac = (x - x0) / (x1 - x0) if x1 != x0 else 0.0
        return y0 + frac * (y1 - y0)
    log_x0 = math.log(x0)
    log_x1 = math.log(x1)
    log_y0 = math.log(y0)
    log_y1 = math.log(y1)
    frac = (math.log(x) - log_x0) / (log_x1 - log_x0) if log_x1 != log_x0 else 0.0
    return math.exp(log_y0 + frac * (log_y1 - log_y0))


def _interp_curve(curve: list[tuple[float, float]], x: float) -> float:
    """Interpolate within a single (x, y) curve using log-log."""
    xs = [p[0] for p in curve]
    ys = [p[1] for p in curve]
    if x <= xs[0]:
        return ys[0]
    if x >= xs[-1]:
        return ys[-1]
    idx = bisect.bisect_right(xs, x)
    return _log_interp(x, xs[idx - 1], xs[idx], ys[idx - 1], ys[idx])



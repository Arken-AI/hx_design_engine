def lookup_factor_A(D_o_t: float, L_D_o: float) -> float:
    """Interpolate Factor A from Table G using log-log interpolation.

    Parameters
    ----------
    D_o_t : float
        Outer diameter to thickness ratio.
    L_D_o : float
        Unsupported length to outer diameter ratio.

    Returns
    -------
    float
        Factor A (dimensionless).

    Raises
    ------
    ValueError
        If D_o_t is outside the range [4, 1000].
    """
    if D_o_t < 4 or D_o_t > 1000:
        raise ValueError(
            f"D_o/t = {D_o_t:.1f} outside Table G range [4, 1000]"
        )

    # Clamp L/D_o to table range
    L_D_o = max(0.05, min(L_D_o, 5.0))

    # Find bracketing D_o/t curves
    if D_o_t <= _DOT_KEYS[0]:
        return _interp_curve(TABLE_G[_DOT_KEYS[0]], L_D_o)
    if D_o_t >= _DOT_KEYS[-1]:
        return _interp_curve(TABLE_G[_DOT_KEYS[-1]], L_D_o)

    idx = bisect.bisect_right(_DOT_KEYS, D_o_t)
    dot_lo = _DOT_KEYS[idx - 1]
    dot_hi = _DOT_KEYS[idx]

    A_lo = _interp_curve(TABLE_G[dot_lo], L_D_o)
    A_hi = _interp_curve(TABLE_G[dot_hi], L_D_o)

    # Log-log interpolation between D_o/t curves
    return _log_interp(D_o_t, dot_lo, dot_hi, A_lo, A_hi)


def lookup_factor_B(
    material: str, temperature_C: float, factor_A: float
) -> tuple[float, bool]:
    """Return (Factor_B_MPa, is_elastic) from the material chart.

    Parameters
    ----------
    material : str
        Material key (must be in MATERIAL_TO_CHART).
    temperature_C : float
        Design temperature in °C.
    factor_A : float
        Factor A from Table G lookup.

    Returns
    -------
    tuple[float, bool]
        (Factor_B in MPa, is_elastic flag).
        is_elastic=True when factor_A falls below the lowest A in the
        temperature curve (elastic buckling regime — use 2AE/(3·D_o/t)).

    Raises
    ------
    KeyError
        If material is not in the chart mapping.
    """
    chart_id = MATERIAL_TO_CHART[material]
    chart = FACTOR_B_CHARTS[chart_id]
    temps = sorted(chart.keys())

    # Find the appropriate temperature curve(s)
    if temperature_C <= temps[0]:
        curve = chart[temps[0]]
    elif temperature_C >= temps[-1]:
        curve = chart[temps[-1]]
    else:
        # Interpolate between two temperature curves
        idx = bisect.bisect_right(temps, temperature_C)
        T_lo, T_hi = temps[idx - 1], temps[idx]
        curve_lo = chart[T_lo]
        curve_hi = chart[T_hi]
        frac = (temperature_C - T_lo) / (T_hi - T_lo)

        # Check elastic condition on both curves
        A_min_lo = curve_lo[0][0]
        A_min_hi = curve_hi[0][0]

        if factor_A < A_min_lo and factor_A < A_min_hi:
            return 0.0, True

        B_lo = _interp_curve(curve_lo, factor_A)
        B_hi = _interp_curve(curve_hi, factor_A)
        B = B_lo + frac * (B_hi - B_lo)
        return B, False

    # Single curve (boundary temperature)
    A_min = curve[0][0]
    if factor_A < A_min:
        return 0.0, True

    B = _interp_curve(curve, factor_A)
    return B, False
